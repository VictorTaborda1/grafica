private void cargarTabla() {
    DefaultTableModel model = new DefaultTableModel(
        new String[]{"ID","Descripci√≥n","Prioridad","Estado"}, 0
    );

    for (Ticket t : sistema.getTickets()) {
        model.addRow(new Object[]{
            t.getId(),
            t.getDescripcion(),
            t.getPrioridad(),
            t.getEstado()
        });
    }

    Tickets.setModel(model);
}
    private void VerDetalleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VerDetalleActionPerformed
        int fila = Tickets.getSelectedRow();

    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione un ticket", "Aviso", JOptionPane.WARNING_MESSAGE);
        return;
    }
    int id = (int) Tickets.getValueAt(fila, 0);
    Ticket t = sistema.buscarTicketPorId(id);

    VentanaDetalle vd = new VentanaDetalle(new JFrame(), true, t);
    vd.setVisible(true);
    }//GEN-LAST:event_VerDetalleActionPerformed

