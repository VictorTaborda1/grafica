    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
         String nombre = txtNombre.getText();
        String email = txtEmail.getText();
        String esp = txtEspecialidad.getText();

        if (nombre.isEmpty() || email.isEmpty() || esp.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Tecnico t = new Tecnico(nombre, email, esp);
        sistema.agregarTecnico(t);
        

        JOptionPane.showMessageDialog(this, "TÃ©cnico agregado correctamente");
        dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

